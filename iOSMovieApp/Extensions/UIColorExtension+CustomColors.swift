    }
    static var backgroundCell: UIColor? {
        return UIColor(named: "BackgroundCellColor")
    }
    static var firstColor: UIColor? {
        return UIColor(named: "FirstColor")
    }
    static var secondDarkColor: UIColor? {
        return UIColor(named: "SecondDarkColor")
